    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double lineCode = Double.parseDouble(txtInputLDC.getText());
        
        LDC ldc = new LDC(lineCode);
        double esfuerzo = ldc.EstimacionEsfuerzo();
        double costo = ldc.EstimacionCosto();
        
        double productividad = ldc.EstimacionProductividad();
        double costo_ldc = ldc.CostoLDC();
        
        txtEsfuerzo.setText(Math.round(esfuerzo * 100.0) / 100.0 + "");
        txtCosto.setText(Math.round(costo * 100.0) / 100.0 + "");
        txtProductividad.setText(Math.round(productividad * 100.0) / 100.0 + "");
        txtCostoLDC.setText(Math.round(costo_ldc * 100.0) / 100.0 + "");
    }//GEN-LAST:event_btnCalcularActionPerformed

